//*Assignment operator
let r = 5; //* the =

//* Addition Assignment

r+=1; //* same as saying r = r + 1

//*Subtraction Assignment

r-=1;//* same as saying r= r- 1

//* Multiplication Assignment

r*=1 //* same as saying r = r * 1 

//* Division Assignment

r/=1; //* r = r / 1 


//* Remainder (modulus) Assignment

r%= 1 //* same as saying r = r % 1. gives you the remainder of your division problem  5 % 3 = 2 


//* Exponential Assignment 

r **=1 //* same as r = r**1